# To calculate the average of three numbers.

firstNum = int(input("Enter the first number: "))
secondNum = int(input("Enter the second number: "))
thirdNum = int(input("Enter the third number: "))

average = (firstNum + secondNum + thirdNum) / 3

print (f"The average of {firstNum}, {secondNum} and {thirdNum} is {average}.")